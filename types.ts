export type Node = {
  type: Type | number;
  start: number;
  len: number;
  /** Starting offset of line content, relative to this line node's start. */
  c_start?: number;
  /** Byte length of line content, excluding line-level markup. */
  c_len?: number;
  next?: Node;
  child?: Node;
  tail?: Node;
  /** Parsed span-level content for block/line nodes when MMD7 provides it. */
  content?: Node;
};

